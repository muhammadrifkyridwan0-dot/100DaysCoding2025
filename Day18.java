package pkg100daycodingisc;

public class Konversi {
    public static void main(String[] args) {
        
        
        //Konverensi otomatis (Tipe data kecil ke besar)
        
        //Wide Cast
        int a = 19;
        long b = a;
        //Secara otomatis a(int) berubah menjadi b(long)
        System.out.println("int ke long: " + b);
         
        
    }
}
