package rifky;

public class OperatorPerbandingan {
    
    public static void main(String[] args) {
        int p = 20;
        int q = 30;
        int r = 20;
        
        //operator ==
        System.out.println("Apakah p == q ? " + (p == q));
        System.out.println("Apakah p == r ? " + (p == r));

        // operator !=
        System.out.println("Apakah p != q ? " + (p != q));
        System.out.println("Apakah p != r ? " + (p != r));
        





    }
    
}
