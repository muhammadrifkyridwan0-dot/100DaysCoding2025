package rifky;

import java.util.Scanner;

public class OperatorPerbandingan {

    public static void main(String[] args) {
        
        // Deklarasi variabel
        Scanner p = new Scanner (System.in);
        
        System.out.print("Masukkan angka: ");
        int a = p.nextInt();
        System.out.print("Masukkan angka: ");
        int b = p.nextInt();
   
    
        //operator >
        System.out.println("Apakah nilai a lebih besar daripada b? : " + (a > b));
        System.out.println("Apakah nilai b lebih besar daripada a? : " + (b > a));
        //operator <
        System.out.println("Apakah nilai a lebih kecil daripada b? : " + (a < b));
        System.out.println("Apakah nilai b lebih kecil daripada a? : " + (b < a));
    
    
    
    
    
    
    
    
    
    }
}

        















    
