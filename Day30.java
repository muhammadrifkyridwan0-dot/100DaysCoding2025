package rifky;
public class OperatorPerbandingan {
    public static void main(String[] args) {
        
        
        int a = 75;
        int b = 40;
        
        System.out.println("Apakah nilai a lebih besar atau sama dengan b?: " + (a >= b));
        System.out.println("Apakah nilai a lebih kecil atau sama dengan b?: " + (a <= b));
    
    }
}
