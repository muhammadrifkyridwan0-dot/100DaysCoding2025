package Rifky;
public class TipeDataKarakter {
  public static main void (String[] args) {

    /*Tipe Data Karakter char dan boolean*/

    /*Tipe data char adalah tipe data yang digunakan untuk mempresentasikan
    karakter tunggal, seperti huruf, angka, atau simbol*/
    
    // contoh:
    
    char a = 'A'; //nilai char menggunakan tanda petik 1 (') 
    System.out.println(a); //akan mencetak nilai (A) 

    /*Tipe data boolean adalah tipe data yang digunakan untuk mempresentasikan nilai kebenaran, 
      yaitu true (benar) atau salah (false)*/

    //contoh: 

    boolean benar = true;
    boolean salah = false;

    System.out.println(benar); //akan mencetak nilai true
    System.out.println(salah); //akan mencetak nilai false

  }

}
    
   
