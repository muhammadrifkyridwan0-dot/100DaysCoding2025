package Rifky;

public class ContohVariabel {
    public static void main(String[] args) {
        // Deklarasi variabel
        int x;
        
        // Inisialisasi variabel
        x = 0;
        System.out.println("Nilai x awal: " + x);
        
        // Update variabel
        x = 50;
        System.out.println("Nilai x setelah diupdate: " + x);
    }
}
